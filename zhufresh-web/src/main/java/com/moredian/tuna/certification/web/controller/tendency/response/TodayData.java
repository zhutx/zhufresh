package com.moredian.tuna.certification.web.controller.tendency.response;

public class TodayData {
	
	private Integer onlines;
	private Integer authTimes;
	private Integer failTimes;
	
	public Integer getOnlines() {
		return onlines;
	}
	public void setOnlines(Integer onlines) {
		this.onlines = onlines;
	}
	public Integer getAuthTimes() {
		return authTimes;
	}
	public void setAuthTimes(Integer authTimes) {
		this.authTimes = authTimes;
	}
	public Integer getFailTimes() {
		return failTimes;
	}
	public void setFailTimes(Integer failTimes) {
		this.failTimes = failTimes;
	}

}

package com.moredian.tuna.certification.web.controller.overview.response;

public class DeviceOnlineData {
	
	private Integer hotelInstalls;
	private Integer hotelOnlines;
	private Integer barInstalls;
	private Integer barOnlines;
	
	public Integer getHotelInstalls() {
		return hotelInstalls;
	}
	public void setHotelInstalls(Integer hotelInstalls) {
		this.hotelInstalls = hotelInstalls;
	}
	public Integer getHotelOnlines() {
		return hotelOnlines;
	}
	public void setHotelOnlines(Integer hotelOnlines) {
		this.hotelOnlines = hotelOnlines;
	}
	public Integer getBarInstalls() {
		return barInstalls;
	}
	public void setBarInstalls(Integer barInstalls) {
		this.barInstalls = barInstalls;
	}
	public Integer getBarOnlines() {
		return barOnlines;
	}
	public void setBarOnlines(Integer barOnlines) {
		this.barOnlines = barOnlines;
	}

}
